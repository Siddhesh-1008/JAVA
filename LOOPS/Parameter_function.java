public class Parameter_function {
    public static void main(String[] args) {
        //(1,5) ARE ARGUMENTS
        int result1=sum(1,5);
        System.out.println("RESULT:-"+result1);

        int result2=sum(2,6);
        System.out.println("RESULT:-"+result2);
    }

    //FUNCTION DEFINITION
    public static int sum(int first,int second){
        int sum=first + second;
        return sum;
    }
}
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                    