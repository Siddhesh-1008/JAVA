public class for_loop {
    public static void main(String[] args) {
        //PRINT NUMBERD FROM 1 TO 100
        //INTIALIZE i=1
        //THEN CHECK CONDITION 1<=100
        //THEN PRINT STATEMENT System.out.print(i + " ");
        //THEN UPDATION(INCREMENT i)BY 1 i++
        //THEN CHECK CONDITION->{}->UPDATION
        for(int i=1;i<=100;i++)
        {
            System.out.print(i + " ");
        }
        System.out.println();
        
        //PRINT NUMBER FROM 1 TO 100 WITH A INCREMENT OF 2
        for(int j=1;j<=100;j+=2)
        {
            System.out.print(j + " ");
        }
    }
    
}
