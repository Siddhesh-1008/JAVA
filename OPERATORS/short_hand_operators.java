public class short_hand_operators {
    public static void main(String[] args) {
        //INITIALIZE VARIABLE a
        int a=10;
        //a=a+10 CAN ALSO BE WRITTEN AS a+=10
        a+=10;
        System.out.println(a);
        a+=5;
        System.out.println(a);
        System.out.println("SHOTHAND MULTIPLICATION");
        int b=2;
        b*=2;
        System.out.println(b);
    }
}
