class Lec1 {

    public static void main(String[] args) {
        // PRINT ON SAME LINE
        System.out.print("HELLO");
        // PRINT ON NEW LINE
        System.out.println("HELLO WORLD");
        System.out.println("HELLO WORLD AGAIN");

        // PRINT STARS 
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        // USING ONE PRINT
        System.out.print("*\n* * \n* * *\n* * * *\n");
    }

}
