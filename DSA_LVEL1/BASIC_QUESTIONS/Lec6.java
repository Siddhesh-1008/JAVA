
public class Lec6 {

    public static void main(String[] aargs) {
        int i = 1;

        // WHILE WILL RUN UNTIL CONDITION IS TRUE AND STOP WHEN IT IS FALSE
        while (i <= 4) {
            System.out.println(i);
            i++;
        }

        // IN FOR LOOP FIRST INITIALIZE THEN CHECK THEN PRINT THEN INCREMENT 
        // THEN CHECK PRINT INCREMENT
        for (int j = 1; j <= 9; j++) {
            System.out.println(j);
        }
    }
}
