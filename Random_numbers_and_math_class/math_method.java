public class math_method{
    public static void main(String[] args) {
        System.out.println(Math.abs(-99));
        //ROUNDS UP MOVE TO NEXT INTEGER
        System.out.println(Math.ceil(99.2));
        //ROUNDS DOWN REMOVES INTEGER AFTER DECIMALS 
        System.out.println(Math.floor(99.2));
        //ROUNDS UP OR DOWN BASED ON DECIMAL VALUES IF DECIMAL IS ABOVE 5 THEN ROUND UP BELOW 5 THEN ROUND UP
        System.out.println(Math.round(87.5));
        System.out.println(Math.round(87.4));
        



    }
}