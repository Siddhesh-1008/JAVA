public class escape {
    public static void main(String[] args) {
        //TO SHOW SINGLE DOUBLE QUOTES INSIDE "" THEN MAKE USE OF ESCAPE SEQUENCE AND ADD IT AT BEFORE SINGLE OR DOUBLE QUOTES BASICLLY IT IGNORES FURTHER " OR '
        System.out.println("HELLO \"SIDDHESH\"..");
        System.out.println("HELLO \'SIDDHESH\'..");
        //TO HAVE \ IN STRING THEN WE CAN MAKE USE OF \\
        System.out.println("HELLO \\SIDDHESH\\..");
        //INSERT A NEW LINE AFTER A PARTICULAR TEXT
        System.out.println();
        System.out.println("HI \nHELLO \nSIDDHESH");
        System.out.println();
        //TO HAVE A TAB SPACE BETWEEN THE TEXT 
        System.out.println("HI \tHELLO \tSIDDHESH");
        //BACK SLASH BASICALLY MOVE THE SLASH BEFORE A PARTICULAR CHARACTER
        System.out.println("HI\bRAJU");
    }
}
